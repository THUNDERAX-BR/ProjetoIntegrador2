    private void BtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtExcluirActionPerformed
        try {
            String input = JOptionPane.showInputDialog(null, "Excluir este livro irá também excluir todos os exemplares deste cadastrados.\nEscreva exatamente (SIM) para confirmar, qualquer outro texto irá cancelar.");
            if (input.equals("SIM")) {
                LivrosDAO livrosDao = new LivrosDAO(Conector.conectar());
                livrosDao.excluir(id);
                listener.sinalGlobal();
            } else {
                JOptionPane.showMessageDialog(null, "Exclusão cancelada.");
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Exclusão cancelada.");
        }
    }//GEN-LAST:event_BtExcluirActionPerformed

