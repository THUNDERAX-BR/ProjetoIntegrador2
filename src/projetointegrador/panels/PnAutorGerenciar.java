    private void BtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtExcluirActionPerformed
        try {
            String input = JOptionPane.showInputDialog(null, "Excluir este autor irá também excluir todos os livros e exemplares deste cadastrados.\nEscreva exatamente (SIM) para confirmar, qualquer outro texto irá cancelar.");
            if (input.equals("SIM")) {
                AutoresDAO autoresDao = new AutoresDAO(Conector.conectar());
                autoresDao.excluir(id);
                listener.sinalGlobal();
            } else {
                JOptionPane.showMessageDialog(null, "Exclusão cancelada.");
            }
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Exclusão cancelada.");
        }
    }//GEN-LAST:event_BtExcluirActionPerformed

